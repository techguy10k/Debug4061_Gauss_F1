#include "LCD.h"



void LCD_WriteCommand(uint8_t Command)
{
  HAL_GPIO_WritePin(LCD_CS_GPIO_Port,LCD_CS_Pin,GPIO_PIN_RESET);
  HAL_GPIO_WritePin(LCD_RS_GPIO_Port,LCD_RS_Pin,GPIO_PIN_RESET);
  HAL_SPI_Transmit(&hspi1,&Command,1,10);
  HAL_GPIO_WritePin(LCD_CS_GPIO_Port,LCD_CS_Pin,GPIO_PIN_SET);
}

void LCD_WriteDdata(uint8_t Data)
{
  HAL_GPIO_WritePin(LCD_CS_GPIO_Port,LCD_CS_Pin,GPIO_PIN_RESET);
  HAL_GPIO_WritePin(LCD_RS_GPIO_Port,LCD_RS_Pin,GPIO_PIN_SET);
  HAL_SPI_Transmit(&hspi1,&Data,1,100);
  HAL_GPIO_WritePin(LCD_CS_GPIO_Port,LCD_CS_Pin,GPIO_PIN_SET);
}


/* 写一定长度字符 */
void LCD_WriteLine(uint8_t* data,uint16_t size,uint8_t PosY,uint8_t PosX)
{	
	LCD_SetPointerPositon(PosY,PosX);
	for(uint8_t counter = 0;counter < size * 4 ;counter ++)
	{
		LCD_WriteDdata(data[counter]);
//		PosX += 4;
//		LCD_SetColumnAddress(PosX);
	}
}


/* 设置列地址 */
void LCD_SetColumnAddress(uint8_t ColumnAddress)
{
	/* 写列地址低4位 */
	LCD_WriteCommand(0x00 | (ColumnAddress & 0x0f));
	/* 写列地址高4位 */
	LCD_WriteCommand(0x10 | ((ColumnAddress & 0xf0) >> 4));
}

/* 设置页地址 */
void LCD_SetPageAddress(uint8_t PageAddress)
{
	LCD_WriteCommand(0xb0 | (PageAddress & 0x0f));
}

/* 设置LCD数据指针位置 */
void LCD_SetPointerPositon(uint8_t PageAddress,uint8_t ColumnAddress)
{
	LCD_SetColumnAddress(ColumnAddress);
	LCD_SetPageAddress(PageAddress);
}


/* LCD刷新所有像素 */
void LCD_UpdateAllPixel(uint8_t* Address)
{
	LCD_SetPointerPositon(0,0);
	
	for(uint16_t counter = 0;counter < 1536;counter ++)
	{
		LCD_WriteDdata(Address[counter]);
	}
	
	
} 


void LCD_Init(void)
{
  /* LCD复位电平要求 RST低电平有效 至少1ms */
  /* 拉低片选 */
  HAL_GPIO_WritePin(LCD_CS_GPIO_Port,LCD_CS_Pin,GPIO_PIN_RESET);
  HAL_GPIO_WritePin(LCD_RST_GPIO_Port,LCD_RST_Pin,GPIO_PIN_RESET);
  HAL_Delay(50);
  HAL_GPIO_WritePin(LCD_RST_GPIO_Port,LCD_RST_Pin,GPIO_PIN_SET);
  HAL_GPIO_WritePin(LCD_CS_GPIO_Port,LCD_CS_Pin,GPIO_PIN_SET);
	HAL_Delay(50);
  /* 上电复位结束 */
	
	/* 上电显示屏设置相关 */
  LCD_WriteCommand(0xe2); //软件复位
  HAL_Delay(50);           
  LCD_WriteCommand(0x2f); //开启内部升压电路
  HAL_Delay(50);
  LCD_WriteCommand(0xa3); //不知道是什么作用 再尝试(设置帧率?)
  LCD_WriteCommand(0x81); //微调对比度
  LCD_WriteCommand(0x80); //微调对比度值
  LCD_WriteCommand(0xeb); // 1/9偏压 不知道有啥用
  LCD_WriteCommand(0xc4); //行列扫描顺序 从上到下
	
	//LCD_WriteCommand(0xaf); //刷新完屏幕再 避免上电随机数导致乱码 打开显示使能
	

	/* 让屏幕全白 */
	LCD_SetPointerPositon(0,0);
	for(uint16_t Counter = 0;Counter < 1536;Counter ++)
	{
		LCD_WriteDdata(0x00);
	} 
	LCD_SetPointerPositon(0,0);
	
	LCD_WriteCommand(0xaf); //刷新完屏幕再 避免上电随机数导致乱码 打开显示使能
	
	HAL_Delay(50);
	
	
	
	
}

